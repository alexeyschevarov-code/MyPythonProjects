import random

print("=== Генератор случайных исторических историй ===\n")

heroes = [
    "генерал Гудериан", "маршал Жуков", "фельдмаршал Манштейн", 
    "генерал Конев", "генерал Хейнрице", "танкист", "лётчик", 
    "разведчик", "командир танковой дивизии"
]

events = [
    "встретил неожиданного союзника", "принял рискованное решение", 
    "организовал блестящую оборону", "провёл стремительную атаку", 
    "нашёл важный документ", "спас отряд от окружения", 
    "разработал новую тактику", "провёл разведку в тылу врага"
]

places = [
    "на Восточном фронте", "в заснеженных лесах", "в разрушенном городе", 
    "на Курской дуге", "в Нормандии", "в степях Украины", 
    "на берегу Волги", "в Арденнах", "в Крыму"
]

outcomes = [
    "и изменил ход битвы", "и получил высокую награду", 
    "но потерял много техники", "и стал легендой", 
    "и помог союзникам", "но попал в сложную ситуацию", 
    "и спас тысячи солдат", "и захватил стратегический объект"
]

while True:
    input("Нажми Enter для нового события...")
    hero = random.choice(heroes)
    event = random.choice(events)
    place = random.choice(places)
    outcome = random.choice(outcomes)
    
    print(f"📖 История: {hero} {event} {place} {outcome}.")
    print("-" * 60)
    
    again = input("Ещё одну историю? (Enter — да, 'выход' — нет): ").lower().strip()
    if again == "выход" or again == "exit":
        print("До встречи! 👋")
        break