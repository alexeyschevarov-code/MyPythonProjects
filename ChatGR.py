def load_history():
    """Загружает имя, статистику и историю из файла."""
    global name, mood_responses_count, history
    if not HISTORY_FILE.exists():
        return
    try:
        with open(HISTORY_FILE, encoding="utf-8") as f:
            data = json.load(f)
        name = data.get("name")
        mood_responses_count = data.get("mood_responses_count", 0)
        history = data.get("history", [])
    except (json.JSONDecodeError, OSError):
        history = []


def save_history():
    """Сохраняет всё в файл после каждого сообщения."""
    data = {
        "version": "0.1.0",
        "name": name,
        "total_messages": len(history),
        "mood_responses_count": mood_responses_count,
        "last_chat": datetime.now().strftime("%Y-%m-%d %H:%M"),
        "history": history[-100:],
    }
    try:
        with open(HISTORY_FILE, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
    except OSError:
        pass


def add_message(user_text, bot_text):
    """Добавляет пару сообщений в историю."""
    history.append({
        "time": datetime.now().strftime("%H:%M"),
        "user": user_text,
        "bot": bot_text,
    })


def bot_reply(text, user_text="", use_name=False):
    """Печатает ответ бота и сохраняет в историю."""
    # Имя добавляем только когда явно нужно — не дублируем в прощании и статистике
    if use_name and name and name.lower() not in text.lower() and random.random() < 0.3:
        text = f"{name}, {text[0].lower()}{text[1:]}" if text else text
    print("Бот:", text)
    if user_text:
        add_message(user_text, text)
        save_history()
    return text


def words_match_root(words, root):
    return any(word.startswith(root) for word in words)


def find_topic(user_input, words):
    age_hints = (
        "когда ты родился", "когда родился", "когда ты появился",
        "когда появился", "когда создан", "когда тебя создали",
        "дата рождения", "дата создания",
    )
    if ("сколько" in words and "лет" in words) or any(h in user_input for h in age_hints):
        return "возраст"
    for topic, roots in topic_roots:
        for root in roots:
            if words_match_root(words, root):
                return topic
    return None


def find_mood(words):
    for mood in sorted(mood_responses, key=len, reverse=True):
        if mood in words:
            return mood
    return None


def show_recent_history(user_input="история"):
    recent = history[-6:]
    if not recent:
        bot_reply("История пока пуста — мы только начали разговор!", user_input)
        return
    lines = ["Последние сообщения:"]
    for item in recent:
        lines.append(f"  [{item['time']}] Ты: {item['user']}")
        lines.append(f"  [{item['time']}] Бот: {item['bot']}")
    bot_reply("\n".join(lines), user_input)


