    if "помощь" in user_input or "команды" in user_input:
        bot_reply(random.choice(responses["помощь"]), user_input)
        continue

    if user_input in ("сброс имени", "забудь имя", "забыть имя"):
        name = None
        bot_reply("Хорошо, забыл твоё имя. Представься заново: напиши «меня зовут ...»", user_input)
        continue

    if "меня зовут" in user_input:
        rest = user_input.split("меня зовут", 1)[1].strip(" .,!?")
        if rest:
            name = rest.split()[0].capitalize()
            bot_reply(
                f"Приятно познакомиться, {name}! Запомнил — теперь буду обращаться только так.",
                user_input,
            )
        continue

    if any(p in user_input for p in ("как тебя зовут", "тебя зовут", "твоё имя", "твое имя")):
        if name:
            bot_reply(f"Я ChatGR. А тебя я знаю — тебя зовут {name}!", user_input)
        else:
            bot_reply(random.choice(responses["имя"]), user_input)
        continue

    if name and any(p in user_input for p in ("как меня зовут", "моё имя", "мое имя", "помнишь меня")):
        bot_reply(f"Конечно помню — тебя зовут {name}!", user_input)
        continue

    found = False

    mood = find_mood(words)
    if mood:
        bot_reply(random.choice(mood_responses[mood]), user_input)
        mood_responses_count += 1
        found = True

    if not found:
        topic = find_topic(user_input, words)
        if topic:
            bot_reply(random.choice(responses[topic]), user_input, use_name=True)
            found = True

    if not found:
        bot_reply(
            "Интересно... Расскажи подробнее! Или напиши «помощь» — покажу, о чём могу говорить.",
            user_input,
        )