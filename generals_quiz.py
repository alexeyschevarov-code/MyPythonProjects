import random

questions = [
    {"q": "Кто считается отцом тактики блицкрига?", "a": "Гудериан", "options": ["Манштейн", "Гудериан", "Жуков", "Конев"]},
    {"q": "Кто разработал план 'Удар по тылам' в 1943 году?", "a": "Манштейн", "options": ["Гудериан", "Жуков", "Манштейн", "Хейнрице"]},
    {"q": "Кто руководил обороной Москвы в 1941 году?", "a": "Жуков", "options": ["Конев", "Жуков", "Манштейн", "Гудериан"]},
    {"q": "Кто освободил большую часть Украины в 1943-1944 годах?", "a": "Конев", "options": ["Жуков", "Конев", "Хейнрице", "Гудериан"]},
    {"q": "Кто был мастером обороны на Зееловских высотах в 1945 году?", "a": "Хейнрице", "options": ["Гудериан", "Манштейн", "Хейнрице", "Жуков"]},
]

random.shuffle(questions)
score = 0

print("=== Викторина: Генералы Второй мировой ===\n")

for i, q in enumerate(questions, 1):
    print(f"Вопрос {i}: {q['q']}")
    for idx, opt in enumerate(q['options']):
        print(f"{chr(65+idx)}. {opt}")
    answer = input("Твой ответ (A/B/C/D): ").upper().strip()
    
    if answer == chr(65 + q['options'].index(q['a'])):
        print("✅ Правильно!\n")
        score += 1
    else:
        print(f"❌ Неправильно. Правильный ответ: {q['a']}\n")

print(f"Результат: {score} из {len(questions)}")
