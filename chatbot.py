import random

print("=== Универсальный Чат-бот ===")
print("Напиши 'пока' или 'выход', чтобы закончить разговор\n")

name = None

responses = {
    "привет": ["Привет!", "Здравствуй!", "Рад тебя видеть!"],
    "дела": ["Нормально, спасибо! А у тебя?", "Всё хорошо, а как твои дела?"],
    "имя": ["Меня зовут простой чат-бот. А тебя как?"],
    "возраст": ["Я создан в 2026 году, так что мне меньше года 😄"],
    "игра": ["Люблю компьютерные игры! Какая у тебя любимая?"],
    "война": ["История войн — интересная тема. Что тебя интересует?"],
    "вв2": ["Вторая мировая война — очень важная тема в истории."],
    "ww2": ["Вторая мировая война — очень важная тема в истории."],
    "погода": ["Я не могу проверить погоду, но надеюсь, у тебя хорошая!"],
    "музыка": ["Музыка — это круто! Какой жанр тебе нравится?"],
    "книга": ["Книги — отличный способ провести время. Что читаешь сейчас?"],
    "фильм": ["Фильмы — моя слабость. Какой жанр любишь?"]
}

while True:
    user_input = input("Ты: ").lower().strip()
    
    if "пока" in user_input or "выход" in user_input:
        print("Бот: До встречи! 👋")
        break
    
    # Запоминаем имя
    if "зовут" in user_input or "имя" in user_input:
        if name is None:
            name = input("Бот: Как тебя зовут? ").strip()
            print(f"Бот: Приятно познакомиться, {name}!")
        else:
            print(f"Бот: Тебя зовут {name}, верно?")
        continue
    
    found = False
    for key in responses:
        if key in user_input:
            print("Бот:", random.choice(responses[key]))
            found = True
            break
    
    if not found:
        print("Бот: Интересно... Расскажи подробнее!")