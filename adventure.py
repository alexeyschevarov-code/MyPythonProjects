import random

def play_game():
    print("\n=== Текстовое приключение: Загадочный лес ===\n")
    print("Ты проснулся в неизвестном лесу. Что будешь делать?\n")
    
    choice1 = input("1. Пойти налево (к старой хижине)\n2. Пойти направо (к реке)\n3. Пойти прямо (в чащу)\nТвой выбор (1/2/3): ")
    
    if choice1 == "1":
        print("\nТы нашёл старую хижину.")
        choice2 = input("1. Зайти внутрь\n2. Обойти хижину\nТвой выбор: ")
        if choice2 == "1":
            print("\nВ хижине ты нашёл карту и фонарик. Ты успешно вышел из леса! 🎉")
        else:
            print("\nТы нашёл тропинку за хижиной и вышел к деревне.")
    
    elif choice1 == "2":
        print("\nТы вышел к реке.")
        choice2 = input("1. Переплыть реку\n2. Пойти вдоль реки\n3. Построить плот\nТвой выбор: ")
        if choice2 == "1":
            print("\nТы успешно переплыл реку и нашёл дорогу! 🎉")
        elif choice2 == "2":
            print("\nТы нашёл мост и спокойно перешёл.")
        else:
            print("\nПлот развалился, но ты доплыл до другого берега.")
    
    else:
        print("\nТы пошёл в чащу и встретил старого охотника.")
        choice2 = input("1. Попросить помощи\n2. Пойти дальше самому\nТвой выбор: ")
        if choice2 == "1":
            print("\nОхотник показал тебе путь из леса. Ты спасён! 🎉")
        else:
            print("\nТы заблудился, но в конце концов нашёл выход.")
    
    print("\nКонец приключения!")

# Главный цикл
while True:
    play_game()
    again = input("\nХочешь сыграть ещё раз? (да/нет): ").lower()
    if again != "да":
        print("Спасибо за игру! До встречи! 👋")
        break